void toggleAlarm(uint8_t a) {
	armed = a;
	ST7735_SetCursor(5, 13);
	ST7735_OutString("        ");
	ST7735_SetCursor(5, 13);
	if(armed){
		ST7735_OutString("Armed");
	} else {
		ST7735_OutString("Set");
	}
}

uint8_t isArmed(void) {
	return armed;
}

void displayAlarm(uint8_t min, uint8_t hour){
	alarmH = hour;
	alarmM = min;
	
	ST7735_SetCursor(5, 12);
	ST7735_OutString("Alarm:");
	
	ST7735_SetCursor(5, 14);
	if(alarmH < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(alarmH);
	ST7735_OutString(":");
	if(alarmM < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(alarmM);
}

void updateClock(uint8_t min, uint8_t hour) {
	ST7735_SetCursor(15, 13);
	ST7735_OutString("Set:");
	minutes = min;
	hours = hour;
	isStale = 1;
}

void clearSet(void) {
	ST7735_SetCursor(15, 13);
	ST7735_OutString("     ");
}

void displayClock(void) {
	PF3 ^= 0x01;
	PF3 ^= 0x01;
	uint8_t m = minutes;
  uint8_t h = hours;
	isStale = 0;
	ST7735_DrawBitmap(30, 100, clockFace, 80, 80);
	ST7735_DrawLine(CenterX, CenterY, CenterX+(Rad * sin((double)m* 2.0 * PI/60.0)), CenterY - (Rad * cos((double)m* 2.0 * PI/60.0)), ST7735_BLUE);
	ST7735_DrawLine(CenterX, CenterY, CenterX+(RadH * sin((double)(h%12 + m/60)* 2.0 * PI/12.0)), CenterY - (RadH * cos((double)(h%12 + m/60)* 2.0 * PI/12.0)), ST7735_RED);

	ST7735_SetCursor(15, 14);
	if(hours < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(hours);
	ST7735_OutString(":");
	if(minutes < 10) {
		ST7735_OutUDec(0);
	}
	ST7735_OutUDec(minutes);
	PF3 ^= 0x01;
}

void snooze(void) {
	alarmM += 5;
}

void tickClock(void) {
	long prevState = StartCritical();
	subStale = 1;
	sec += 1;
	
	if (sec >= 60){
		minutes += 1;
		sec = 0;
		isStale = 1;
	}
	
	if (minutes >= 60){
		hours += 1;
		minutes = 0;
		isStale = 1;
	}
	
	if (hours >= 24) {
		hours = 0;
		isStale = 1;
	}
	if(alarmH == hours && alarmM == minutes){
		speakerStart();
	} else {
		speakerStop();
	}
	
	EndCritical(prevState);
}
